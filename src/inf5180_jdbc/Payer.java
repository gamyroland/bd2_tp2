    private void bouton_payerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bouton_payerActionPerformed
       
        try {
            int noLivraison = Integer.parseInt( textbox_no_livraison.getText() );
            float montant = Float.parseFloat( textbox_montant.getText() );

            System.out.println("no livra" + noLivraison);

            if (radio_carte.isSelected()) {
                int no_carte = Integer.parseInt(textbox_no_carte.getText());
                float soldeAvant = trouverSoldeRestant(noLivraison);


                System.out.println("solde avant" + soldeAvant);


                if ( validerMontant(montant, soldeAvant) ) {
                    float soldeApres = soldeAvant - montant;
                    payer( soldeAvant, soldeApres, noLivraison);
                    payerCarteCredit( noLivraison, dropdown_carte.getSelectedItem().toString(), no_carte, montant );
                }                

            } else if ( radio_cheque.isSelected() ) {
                int no_cheque = Integer.parseInt(textbox_no_cheque.getText());
                float soldeAvant = trouverSoldeRestant(noLivraison);


                System.out.println("solde avant" + soldeAvant);

                
                if ( validerMontant(montant, soldeAvant) ) {
                    float soldeApres = soldeAvant - montant;
                    payer( soldeAvant, soldeApres, noLivraison);
                    payerCheque( noLivraison, textbox_banque.getText(), no_cheque, montant );
                }
            }
        }catch (NumberFormatException i){
            JOptionPane.showMessageDialog(this, "Un type de donn√©e est incorrect!", "ERREUR", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            System.err.println("SQLException: " + ex.getMessage());
        }

    }//GEN-LAST:event_bouton_payerActionPerformed

    

    public void payerCarteCredit(int noLivraison, String typeDeCarte, int noCarte, float montant) throws SQLException {

        JDBCDriverInformation.unEnonceSQL = JDBCDriverInformation.con.createStatement();

        String datePaiement = dateAujourdhui();
        String sql_insert = "INSERT INTO PaiementCarteCredit (noCarte, typeDeCarte, datePaiement, montant, noLivraison)"
                         + " VALUES ('"
                         + String.valueOf(noCarte) + "', '"
                         + typeDeCarte  + "', '"
                         + datePaiement + "', "
                         + montant      + ", "
                         + noLivraison  + ")";

        int rangeesModifiees = JDBCDriverInformation.unEnonceSQL.executeUpdate(sql_insert);
        JDBCDriverInformation.unEnonceSQL.close();        
    }



    public void payerCheque(int noLivraison, String banque, int noCheque, float montant) throws SQLException {

        JDBCDriverInformation.unEnonceSQL = JDBCDriverInformation.con.createStatement();

        String datePaiement = dateAujourdhui();

        Date date = new Date();
        String sql_insert = "INSERT INTO PaiementCheque (noCheque, banque, datePaiement, montant, noLivraison)"
                         + " VALUES ('"
                         + String.valueOf(noCheque) + "', '"
                         + banque       + "', '"
                         + date + "', "
                         + montant      + ", "
                         + noLivraison  + ")";

        int rangeesModifiees = JDBCDriverInformation.unEnonceSQL.executeUpdate(sql_insert);
        JDBCDriverInformation.unEnonceSQL.close();
    }



    public void payer(float soldeAvant, float soldeApres, int noLivraison) throws SQLException {
        
        JDBCDriverInformation.unEnonceSQL = JDBCDriverInformation.con.createStatement();

        String sql_payer = "UPDATE FactureLivraison"
                         + " SET soldeRestant = " + soldeApres
                         + " WHERE noLivraison = " + noLivraison;

        int rangeesModifiees = JDBCDriverInformation.unEnonceSQL.executeUpdate(sql_payer);
        JDBCDriverInformation.unEnonceSQL.close();

        textbox_avant.setText(String.valueOf(soldeAvant));
        textbox_apres.setText(String.valueOf(soldeApres));
    }



    public boolean validerMontant(float montant, float soldeRestant) {
        if ( montant > soldeRestant ) {
            textbox_avant.setText(String.valueOf(soldeRestant));
            textbox_apres.setText("");
            JOptionPane.showMessageDialog(this, "Le montant vers√© exc√®de le solde restant!", "ERREUR!", JOptionPane.ERROR_MESSAGE);            
            return false;
        } else {
            return true;
        }
    }



    public static float trouverSoldeRestant(int noLivraison) throws SQLException {
        
        JDBCDriverInformation.unEnonceSQL = JDBCDriverInformation.con.createStatement();

        String sql_soldeRestant = "SELECT soldeRestant FROM FactureLivraison WHERE noLivraison = " + noLivraison;

        ResultSet resultatSelect = JDBCDriverInformation.unEnonceSQL.executeQuery(sql_soldeRestant);
        //JDBCDriverInformation.unEnonceSQL.close();

        if ( resultatSelect.next() ) {
            return resultatSelect.getFloat("soldeRestant");
        } else {
            return 6666666;
        }
    }



