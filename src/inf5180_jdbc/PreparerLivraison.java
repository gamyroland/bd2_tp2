    public void actionPerformed(ActionEvent e) {

        Object source = e.getSource() ;

         if (source == jButton1){
            if (jRadioButton1.isSelected () ){
                  ConsulterLivraison cosultation = new ConsulterLivraison ("Consulter Livraison") ;
                  cosultation.setVisible (true) ;
            }else if ( jRadioButton2.isSelected() ) {
                String value = JOptionPane.showInputDialog(null , "Entrez le numero du client: ", "Recherche du client", JOptionPane.QUESTION_MESSAGE) ;
                int valeur  ; 
                if (value == null || value.trim().length() == 0){
                    JOptionPane.showMessageDialog(this, "Valeur invalide", "ERREUR", JOptionPane.ERROR_MESSAGE);
                }else  {
                    // creation d'un enonce associ a la connexion
                     try{
                         valeur = Integer.parseInt(value) ;
                          select1 = "Select nom, prenom, plusGrandeQualite, telephone from Client where noClient = " + valeur ;
                          select2 = "Select numeroCivique, rue, ville, pays, codePostal " +
                                  "from Adresse where noClient =" + valeur + " and noFournisseur = 0" ;
                          JDBCDriverInformation.unEnonceSQL  = JDBCDriverInformation.con.createStatement() ;
                          ResultSet resultatSelect = JDBCDriverInformation.unEnonceSQL.executeQuery(select1) ;
                          int nb = 0 ;
                          Client c  = null ;
                          Adresse a = new Adresse () ;
                          if (resultatSelect.next()){
                              nb++ ;
                              c = new Client (valeur,resultatSelect.getString("nom"),
                                       resultatSelect.getString("prenom"),
                                       resultatSelect.getString("plusGrandeQualite"),
                                       resultatSelect.getString("telephone")) ;

                              resultatSelect = JDBCDriverInformation.unEnonceSQL.executeQuery(select2) ;
                              if (resultatSelect.next()){
                                a = new Adresse (resultatSelect.getString("numeroCivique"),
                                        resultatSelect.getString("rue"),
                                        resultatSelect.getString("ville"),
                                        resultatSelect.getString("pays"),
                                        resultatSelect.getString("codePostal")
                                        );

                              }
                          }
                          if (nb == 0)
                               JOptionPane.showMessageDialog(this, "CLIENT INEXISTANT", "ERREUR", JOptionPane.ERROR_MESSAGE);
                          else{
                            AffichageClient client = new AffichageClient ("Affichage du Client", c, a) ;
                            client.setVisible(true) ;
                          }
                     }catch (NumberFormatException i){
                        JOptionPane.showMessageDialog(this, i.getMessage(), "ERREUR", JOptionPane.ERROR_MESSAGE);
                     }catch(SQLException f){
                         JOptionPane.showMessageDialog(this, f.getMessage(), "ERREUR", JOptionPane.ERROR_MESSAGE);
                     }

                 }
               
            } else if (radio_payer.isSelected() ) {
                Payer payer = new Payer("Effecteur un paiement");
                payer.setVisible(true);
            }
            
         } else if (source == jButton2){
             fermerApplication () ;
             this.setVisible (false);
         }
    }
